public class incidencia {
    date fecha;
    double costo;
    String descripcion;
    String tipo;
    public incidencia(){
    }
    public incidencia(date fecha, double costo, String descripcion, String tipo) {
        this.fecha=fecha;
        this.costo=costo;
        this.descripcion=descripcion;
        this.tipo=tipo;
    }
}
